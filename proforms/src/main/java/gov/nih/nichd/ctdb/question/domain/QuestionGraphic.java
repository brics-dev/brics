package gov.nih.nichd.ctdb.question.domain;

public class QuestionGraphic {
	private String questionGraphic;
	private String fileName;
	
	public String getQuestionGraphic() {
		return questionGraphic;
	}

	public void setQuestionGraphic(String questionGraphic) {
		this.questionGraphic = questionGraphic;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

}
